#include "libft.h"

void	ft_putnbr(int n)
{
	long	nbr;

	nbr = n;
	if (nbr >= 0)
		nbr = n;
	if (nbr < 0)
	{
		ft_putchar('-');
		nbr = nbr * (-1);
	}
	if (nbr > 9)
	{
		ft_putnbr(nbr / 10);
		ft_putchar((nbr % 10) + '0');
	}
	else
		ft_putchar(nbr + '0');
}

